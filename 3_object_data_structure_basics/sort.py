# Sort a list, there are 2 methods

# Method one using sorted

list4 = [5,3,4,6,1]

print("First method for list sorting, using sorted:")
print(sorted(list4))
print()

# Method two using the method .sort

list5 = [10,7,1,5,2]
list5.sort()

print("Second method for list sorting, using the .sort method")
print(list5)
